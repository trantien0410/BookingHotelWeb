import axios from "axios";
import { useRouter } from "next/navigation";
import { useCallback, useMemo } from "react";
import { toast } from "react-hot-toast";

import { SafeUser } from "@/app/types";

import useLoginModal from "./useLoginModal";

interface IUseFavorite {
  vehicleId: string;
  currentUser?: SafeUser | null;
}

const useVehicleFavorite = ({ vehicleId, currentUser }: IUseFavorite) => {
  const router = useRouter();

  const loginModal = useLoginModal();

  const hasFavorited = useMemo(() => {
    const list = currentUser?.favoriteIds || [];

    return list.includes(vehicleId);
  }, [currentUser, vehicleId]);

  const toggleFavorite = useCallback(
    async (e: React.MouseEvent<HTMLDivElement>) => {
      e.stopPropagation();

      if (!currentUser) {
        return loginModal.onOpen();
      }

      try {
        let request;

        if (hasFavorited) {
          request = () => axios.delete(`/api/vehicles/favorites/${vehicleId}`);
        } else {
          request = () => axios.post(`/api/vehicles/favorites/${vehicleId}`);
        }

        await request();
        router.refresh();
        toast.success("Success");
      } catch (error) {
        toast.error("Something went wrong.");
      }
    },
    [currentUser, hasFavorited, vehicleId, loginModal, router]
  );

  return {
    hasFavorited,
    toggleFavorite,
  };
};

export default useVehicleFavorite;
